  <h2>Was ist BWD Quiz?</h2>
  <p>BWD Quiz wurde im Rahmen der IDPA von Dominik Schmalstieg und René Meisters entwickelt. Dieses Quiz bietet Lehrern die möglichkeit Multiple Choice Quizze in allen Fächern an der BWD zu erstellen. Die Quizze eignen sich perfekt als Prüfungsvorbereitung. Schüler können die Quizze durchspielen. Am Ende des Quizs werde den Schülern die Note und die richtigen Antworten angezeigt. Um seinen eigenen Fortschritt messen zu können werden alle bisherigen Noten im Quiz angezeigt.
  Sollte mal eine Frage einen Fehler enthalten können Schüler, dass dem Lehrer, der das Quiz erstellt hat ganz einfach mitteilen. Das bearbeiten einer Frage geht dann sehr leicht von der Hand.</p>
